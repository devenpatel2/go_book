// Issueshtml prints an HTML table of issues matching the search terms.
// this code demonstrates use of template with  htmp packag
// to run the code
// go run issueshtml.go repo:golang/go commenter:gopherbot json encoder > /tmp/issues.html

// to view the html page, open the page "/tmp/issues.html" in browser
package main

import (
	"html/template"
	"log"
	"os"
	"github"
)


var issueList = template.Must(template.New("issuelist").Parse(`
<h1>{{.TotalCount}} issues</h1>
<table>
<tr style='text-align: left'>
  <th>#</th>
  <th>State</th>
  <th>User</th>
  <th>Title</th>
</tr>
{{range .Items}}
<tr>
  <td><a href='{{.HTMLURL}}'>{{.Number}}</a></td>
  <td>{{.State}}</td>
  <td><a href='{{.User.HTMLURL}}'>{{.User.Login}}</a></td>
  <td><a href='{{.HTMLURL}}'>{{.Title}}</a></td>
</tr>
{{end}}
</table>
`))



func main() {
	result, err := github.SearchIssues(os.Args[1:])
	if err != nil {
		log.Fatal(err)
	}
	if err := issueList.Execute(os.Stdout, result); err != nil {
		log.Fatal(err)
	}
}